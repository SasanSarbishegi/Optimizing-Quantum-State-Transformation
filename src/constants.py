# Basis matrices for 8x2 Stiefel manifold
def generate_basis_matrices():
    """
    Generate basis matrices for the tangent space projections.

    Returns:
        list: List of 16 basis matrices (8x2 each)
    """
    basis_matrices = []
    for i in range(STIEFEL_DIM_N):
        for j in range(STIEFEL_DIM_P):
            basis_matrix = np.zeros((STIEFEL_DIM_N, STIEFEL_DIM_P), dtype=complex)
            basis_matrix[i, j] = 1
            basis_matrices.append(basis_matrix)
    return basis_matrices

BASIS_MATRICES = generate_basis_matrices()