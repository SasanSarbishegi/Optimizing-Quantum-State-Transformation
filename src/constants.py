STIEFEL_DIM_N = 8
STIEFEL_DIM_P = 2

# Bell state transformation unitary
U_BELL = np.matrix([
    [1,   0,   0,   1],
    [-1j, 0,   0,  1j],
    [0,  -1j, -1j,  0],
    [0,   1,  -1,   0]
]) / np.sqrt(2)

